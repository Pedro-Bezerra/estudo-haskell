{-
    Em Haskell, classes servem para agrupar uma quantidade de funções e compartilhá-las
    entre diferentes tipos (e.g.: Int, Float, String, Bool etc.) que a instanciam. Isto 
    é, para que seja possível utilizar determinadas funções, a exemplo da soma (+), é ne-
    cessário que os operandos sejam instâncias daquela classe, nesse caso, da classe 'Num'.
    Dessa forma, percebe-se como tal conceito é essencial para a programação em Haskell, uma
    vez que essa linguagem é de paradigma funcional.
-}